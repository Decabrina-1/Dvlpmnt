# Функция для подсчета чисел без цифры 3
def count_without_three():
    count = 0

    # Однозначные числа (1-9)
    count += 8  # 1,2,4,5,6,7,8,9

    # Двузначные числа (10-99)
    count += 8 * 9  # первая цифра: 8 вариантов, вторая: 9

    # Трехзначные числа (100-999)
    count += 8 * 9 * 9  # первая:8, вторая:9, третья:9

    # Четырехзначные числа (1000-1999)
    count += 1 * 9 * 9 * 9  # первая:1, остальные:9

    # Четырехзначные числа (2000-2024)
    # Первые две цифры: 20
    # Третья цифра: 0,1,2
    # Четвертая цифра зависит от третьей:
    third_digits = [0, 1, 2]
    for third in third_digits:
        if third < 2:
            # Для третьей цифры 0 или 1: четвертая 0-9 (без 3)
            count += 9
        else:
            # Для третьей цифры 2: четвертая 0-4 (без 3)
            count += 4  # 0,1,2,4

    return count


total_numbers = 2024
without_three = count_without_three()
result = total_numbers - without_three

print(f"Количество чисел с хотя бы одной цифрой 3: {result}")